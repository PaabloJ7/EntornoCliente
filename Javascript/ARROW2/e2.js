const getRandomInt = (max) => Math.floor(Math.random() * max) + 1;

// Ejemplo de uso
const randomValue1 = getRandomInt(10); // Obtendrá un valor aleatorio entre 1 y 10
console.log(randomValue1);

const randomValue2 = getRandomInt(100); // Obtendrá un valor aleatorio entre 1 y 100
console.log(randomValue2);
